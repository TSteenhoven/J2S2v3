public class Coordinaat {

    /*Attribute*/
    int x;
    int y;
    /*constructor*/
    public Coordinaat(int x,int y){
        this.x = x;
        this.y = y;
    }
    /*Methods*/

    //region gs ss
    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
    //endregion
}


